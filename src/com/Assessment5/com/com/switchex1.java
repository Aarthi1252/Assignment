package com.Assessment5.com.com;

public class switchex1 {
    public static void main(String[] args) {
        int num = 50;
        switch (num) {
            case 10:
                System.out.println(10);
                break;
            case 50:
                System.out.println(50);
                break;
            case 30:
                System.out.println(30);
                break;
            default:
                System.out.println("Not in 10,50 or 30");

        }
    }
}
